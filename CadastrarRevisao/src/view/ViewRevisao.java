    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Revisao p = new Revisao();
        p.setCpf(txtCpf.getText());
        p.setModelo(txtModelo.getText());
        p.setAno(Integer.parseInt(txtAno.getText()));
        p.setKm(txtKm.getText());
        p.setData(txtData.getText());
        
        tableModel.adicionaLinha(p);
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        limparCampos();
    }//GEN-LAST:event_btnLimparActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (jTRegistros.getSelectedRow() != -1){
            tableModel.removeLinha(jTRegistros.getSelectedRow());
        }
        limparCampos();
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (jTRegistros.getSelectedRow() != -1){
            tableModel.setValueAt(txtCpf.getText(), jTRegistros.getSelectedRow(), 0);
            tableModel.setValueAt(txtModelo.getText(), jTRegistros.getSelectedRow(), 1);
            tableModel.setValueAt(txtAno.getText(), jTRegistros.getSelectedRow(), 2);
            tableModel.setValueAt(txtKm.getText(), jTRegistros.getSelectedRow(), 3);
            tableModel.setValueAt(txtData.getText(), jTRegistros.getSelectedRow(), 4);            
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

